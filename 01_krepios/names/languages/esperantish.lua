Names.esperantishWord = function()
    local word = Util.random(esperantishRoot)
    local version = math.random(1, 5)
    if version == 1 then
        word = Util.random(esperantishPrefix) .. word
    elseif version == 2 then
        word = word .. Util.random(esperantishInfix)
    end

    word = word .. Util.random(esperantishSuffix)

    word = word:sub(1,1):upper() .. word:sub(2)

    return word
end